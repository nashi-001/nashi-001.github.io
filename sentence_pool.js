// ===== 漢字 → 例文のインデックスを構築 =====
const KANJI_TO_SENTENCES = {};

function buildSentenceIndex() {
    for (let i = 0; i < SENTENCE_POOL.length; i++) {
        const sentence = SENTENCE_POOL[i];
        for (const word of Object.keys(sentence.words)) {
            for (const ch of word) {
                if (!KANJI_TO_SENTENCES[ch]) {
                    KANJI_TO_SENTENCES[ch] = [];
                }
                if (!KANJI_TO_SENTENCES[ch].includes(i)) {
                    KANJI_TO_SENTENCES[ch].push(i);
                }
            }
        }
    }
}

// 初期化時にインデックスを構築
buildSentenceIndex();

// 特定の漢字を含む例文を取得
function getSentencesForKanji(kanji) {
    const indices = KANJI_TO_SENTENCES[kanji] || [];
    return indices.map(i => SENTENCE_POOL[i]);
}

// 特定の漢字を含む例文をランダムに1つ取得
function getRandomSentenceForKanji(kanji) {
    const sentences = getSentencesForKanji(kanji);
    if (sentences.length === 0) {
        // 例文が無い場合、汎用テンプレートを生成
        return generateTemplateSentence(kanji);
    }
    return sentences[Math.floor(Math.random() * sentences.length)];
}

// 例文が無い漢字用のテンプレート生成
function generateTemplateSentence(kanji) {
    const templates = [
        { text: `「${kanji}」という漢字を練習しましょう。`, words: {} },
        { text: `${kanji}の書き方を覚えましょう。`, words: {} },
        { text: `${kanji}を正しく書いてください。`, words: {} }
    ];
    templates[0].words[kanji] = "（かんじ）";
    templates[1].words[kanji] = "（かんじ）";
    templates[2].words[kanji] = "（かんじ）";
    return templates[Math.floor(Math.random() * templates.length)];
}
